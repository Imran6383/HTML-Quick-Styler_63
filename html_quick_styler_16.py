class HTMLPageBuilder:
    """Build styled HTML pages from descriptions with dynamic section counts & custom CSS"""

    @staticmethod
    def extract_sections(description: str) -> List[str]:
        """Extract sections from description (same as before)"""
        sections = []
        section_keywords = {
            'hero': ['hero', 'banner', 'landing'],
            'features': ['features', 'services', 'benefits'],
            'about': ['about', 'team', 'company'],
            'portfolio': ['portfolio', 'gallery', 'work', 'projects'],
            'testimonials': ['testimonials', 'reviews', 'feedback'],
            'pricing': ['pricing', 'plans', 'cost'],
            'contact': ['contact', 'form', 'reach'],
            'footer': ['footer', 'bottom']
        }

        desc_lower = description.lower()
        for section_type, keywords in section_keywords.items():
            for keyword in keywords:
                if keyword in desc_lower:
                    sections.append(section_type)
                    break

        if not sections:
            sections = ['hero', 'features', 'contact', 'footer']

        # Remove duplicates while preserving order
        unique_sections = []
        for section in sections:
            if section not in unique_sections:
                unique_sections.append(section)
        return unique_sections

    @staticmethod
    def extract_counts(description: str) -> Dict[str, int]:
        """Extract numeric hints from description (e.g., '3 features' -> features=3)"""
        counts = {}
        patterns = {
            'features': r'(\d+)\s+features?',
            'testimonials': r'(\d+)\s+testimonials?',
            'portfolio': r'(\d+)\s+portfolio items?',
            'pricing': r'(\d+)\s+pricing plans?'
        }
        desc_lower = description.lower()
        for key, pattern in patterns.items():
            match = re.search(pattern, desc_lower)
            if match:
                counts[key] = int(match.group(1))
        return counts

    @staticmethod
    def generate_meta_tags(title: str, description: str) -> str:
        return f'''    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>{title}</title>
    <meta name="description" content="{description[:160]}">
    <meta name="theme-color" content="#0f172a">
    <meta property="og:title" content="{title}">
    <meta property="og:description" content="{description[:160]}">
    <meta property="og:type" content="website">
    <meta name="twitter:card" content="summary_large_image">
    <meta name="author" content="HTML Quick-Styler v2">'''

