# Global history list
generation_history = []

def generate_html_page(title: str, description: str, style: str,
                       include_toggle: bool,
                       custom_css: str,
                       feature_count: int, testimonial_count: int,
                       portfolio_count: int, pricing_count: int) -> Tuple[str, str, str, str]:
    """Generate HTML page with all parameters."""
    if not title.strip() or not description.strip():
        return "", "❌ Please provide both title and description!", "", ""

    try:
        html_output, palette = HTMLGenerationEngine.generate_page(
            title, description, style, include_toggle, custom_css,
            feature_count, testimonial_count, portfolio_count, pricing_count
        )

        # Format color info
        color_info = f"""**Color Palette for '{style}' style:**

| Element | Color |
|---------|-------|
| Primary | {palette['primary']} |
| Secondary | {palette['secondary']} |
| Accent | {palette['accent']} |
| Background | {palette['background']} |
| Surface | {palette['surface']} |
| Text Primary | {palette['text_primary']} |
| Text Secondary | {palette['text_secondary']} |
| Border | {palette['border']} |"""

        # Add to history (store only metadata, not full HTML to keep state small)
        generation_history.append({
            'title': title,
            'description': description[:50] + "...",
            'style': style,
            'timestamp': datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        })
        if len(generation_history) > 20:
            generation_history.pop(0)

        return html_output, "✅ HTML page generated successfully!", color_info, html_output

    except Exception as e:
        return "", f"❌ Error: {str(e)}", "", ""

def get_color_swatches(style: str) -> str:
    """Return HTML with color swatches for the selected style."""
    try:
        palette = ColorPaletteGenerator.generate_palette(style)
        html_swatch = '<div style="display: grid; grid-template-columns: repeat(auto-fill, minmax(140px,1fr)); gap: 12px; margin-top: 20px;">'
        for name, color in palette.items():
            text_color = '#fff' if int(color[1:3],16) < 128 else '#000'
            html_swatch += f'<div style="background:{color}; padding: 16px 8px; border-radius: 12px; text-align: center; color:{text_color}; font-weight: 500; box-shadow: 0 4px 6px rgba(0,0,0,0.1);"><div style="font-size: 0.9rem; text-transform: capitalize;">{name}</div><div style="font-family: monospace; margin-top: 4px;">{color}</div></div>'
        html_swatch += '</div>'
        return html_swatch
    except Exception as e:
        return f"<p>Error: {e}</p>"

def preview_html(html_content: str) -> str:
    """Wrap HTML in an iframe for live preview."""
    if not html_content or not html_content.strip():
        return "<p style='text-align:center; color:#fff; padding:40px; background:#333; border-radius:10px;'>✨ No HTML to preview. Generate a page first in the 'Generate Page' tab!</p>"
    escaped = html.escape(html_content, quote=True)
    return f'<iframe srcdoc="{escaped}" style="width:100%; height:600px; border:none; border-radius:12px; box-shadow:0 10px 25px rgba(0,0,0,0.2);"></iframe>'

def download_html(html_content: str) -> Optional[str]:
    """Save HTML content to a temporary file and return its path for download."""
    if not html_content:
        return None
    with tempfile.NamedTemporaryFile(mode='w', suffix='.html', delete=False, encoding='utf-8') as f:
        f.write(html_content)
        return f.name

