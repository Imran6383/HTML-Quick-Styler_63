    @staticmethod
    def generate_nav_links(sections: List[str]) -> str:
        """Generate navigation links based on sections present"""
        display_names = {
            'hero': 'Home', 'features': 'Features', 'about': 'About',
            'portfolio': 'Work', 'testimonials': 'Testimonials',
            'pricing': 'Pricing', 'contact': 'Contact'
        }
        links = []
        for section in sections:
            if section in display_names and section != 'footer':
                links.append(f'<a href="#{section}">{display_names[section]}</a>')
        return '\n                '.join(links)

